// Permit cleartext HTTP to 127.0.0.1 ONLY.
//
// The audio shim (worklet/shim.js) serves audio over plain HTTP on localhost so
// ExoPlayer can range-request it. Android has blocked cleartext by default since
// API 28, and that includes loopback, so without this the player silently fails
// to open the stream.
//
// The lazy fix is `usesCleartextTraffic: true`, which permits cleartext to EVERY
// domain and quietly undoes a platform protection for the whole app. We do not
// need that: we need one loopback address. So this emits a network security
// config that allows cleartext for 127.0.0.1 and nothing else.
//
// There is no real secret on that socket anyway (audio the user already has
// access to), and it is loopback-bound so nothing off-device can reach it. But
// scoping it keeps any FUTURE http call in this app honest.

const { withAndroidManifest, withDangerousMod, AndroidConfig } = require('expo/config-plugins')
const fs = require('fs')
const path = require('path')

const XML = `<?xml version="1.0" encoding="utf-8"?>
<!-- Generated by plugins/with-localhost-cleartext.js. Do not edit by hand. -->
<network-security-config>
  <!-- The PearTune audio shim serves the P2P stream to ExoPlayer over loopback. -->
  <domain-config cleartextTrafficPermitted="true">
    <domain includeSubdomains="false">127.0.0.1</domain>
  </domain-config>
  <!-- Everything else keeps the platform default: cleartext denied. -->
  <base-config cleartextTrafficPermitted="false" />
</network-security-config>
`

const withLocalhostCleartext = (config) => {
  config = withDangerousMod(config, [
    'android',
    async (cfg) => {
      const dir = path.join(cfg.modRequest.platformProjectRoot, 'app/src/main/res/xml')
      fs.mkdirSync(dir, { recursive: true })
      fs.writeFileSync(path.join(dir, 'network_security_config.xml'), XML)
      return cfg
    }
  ])

  config = withAndroidManifest(config, (cfg) => {
    const app = AndroidConfig.Manifest.getMainApplicationOrThrow(cfg.modResults)
    app.$['android:networkSecurityConfig'] = '@xml/network_security_config'
    // Explicitly false: the network security config is what grants loopback, and
    // leaving this true would make the scoping above pointless.
    app.$['android:usesCleartextTraffic'] = 'false'
    return cfg
  })

  return config
}

module.exports = withLocalhostCleartext
